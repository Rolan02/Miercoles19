public class Elementos {
    
    private int digito;
    private int posicion;

    public Elementos(int digito, int posicion) {
        this.digito = digito;
        this.posicion = posicion;
    }
    
    
    public int getDigito() {
        return digito;
    }

    public int getPosicion() {
        return posicion;
    }
    
}
